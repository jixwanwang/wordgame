export function getRandomWord(lengths: number[]): string {
    if (lengths.some((length) => DICTIONARY[length] == null)) {
        throw new Error("not valid length");
    }

    const randomLength = lengths[Math.floor(Math.random() * lengths.length)];
    console.log("*******", lengths, randomLength);
    const words = DICTIONARY[randomLength];
    return words[Math.floor(Math.random() * words.length)];
}

export function getRandomWords(count: number, lengths: number[]): string[] {
    const words = new Set<string>();

    while (words.size < count) {
        words.add(getRandomWord(lengths));
    }

    return Array.from(words);
}

export function getAllWords(): string[] {
    return Object.values(DICTIONARY).flat();
}

export function getWordsByLength(length: number): string[] {
    return DICTIONARY[length] || [];
}

export function getRandomAbbreviation(length?: number): string {
  if (length) {
    const words = ABBREVIATIONS[length];
    if (!words || words.length === 0) {
      throw new Error(`No abbreviations of length ${length}`);
    }
    return words[Math.floor(Math.random() * words.length)];
  }

  // If no length specified, pick a random length first
  const lengths = Object.keys(ABBREVIATIONS).map(Number);
  const randomLength = lengths[Math.floor(Math.random() * lengths.length)];
  return getRandomAbbreviation(randomLength);
}

export function getAllAbbreviations(): string[] {
  return Object.values(ABBREVIATIONS).flat();
}

export function getAbbreviationsByLength(length: number): string[] {
  return ABBREVIATIONS[length] || [];
}
